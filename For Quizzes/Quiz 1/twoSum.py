# Two Sum

def twoSum(A, t):
    n = len(A)
    i, j = 0, n-1
    while i < j:
        if A[i] + A[j] == t:
            return (i, j)
        elif A[i] + A[j] < t:
            i += 1
        else:
            j -= 1

