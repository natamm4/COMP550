def select_ints(A):
    n = len(A)
    i = 1
    while i <= n:
        m = i
        j = i + 1
        while j <= n:
            if A[j][1] < A[m][1]:
                m = j
        was_i = A[i][1]
        was_j = A[j][1]
        A[i][1] = was_j
        A[j][1] = was_i
    S = []
    k = 0
    for e in A:
        if A[e][0] >= S[k][1]:
            k += 1
            S[k] = A[e]
        k -= 1
    return S

A1 = [[3, 5], [1, 2], [2, 4]]
print(select_ints(A1))

